#include <iostream>
using namespace std;

int main() {
    float n1, n2;

    cout << "Escribe un numero: ";
    cin >> n1;

    cout << "Escribe otro numero: ";
    cin >> n2;

    cout << "La suma es: " << n1 + n2 << endl;
    cout << "La resta es: " << n1 - n2 << endl;
    cout << "La multiplicacion es: " << n1 * n2 << endl;
    cout << "La division es: " << n1 / n2 << endl;

    return 0;
}
